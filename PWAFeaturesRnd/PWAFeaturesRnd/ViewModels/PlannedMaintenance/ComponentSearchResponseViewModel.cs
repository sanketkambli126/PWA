namespace PWAFeaturesRnd.ViewModels.PlannedMaintenance
{
    /// <summary>
    /// Component Search Response ViewModel
    /// </summary>
    public class ComponentSearchResponseViewModel
    {
        /// <summary>
        /// Gets or sets the component code.
        /// </summary>
        /// <value>
        /// The component code.
        /// </value>
        public string ComponentCode { get; set; }

        /// <summary>
        /// Gets or sets the name of the component.
        /// </summary>
        /// <value>
        /// The name of the component.
        /// </value>
        public string ComponentName { get; set; }

        /// <summary>
        /// Gets or sets the component position.
        /// </summary>
        /// <value>
        /// The component position.
        /// </value>
        public string ComponentPosition { get; set; }

        /// <summary>
        /// Gets or sets the name of the maker.
        /// </summary>
        /// <value>
        /// The name of the maker.
        /// </value>
        public string MakerName { get; set; }

        /// <summary>
        /// Gets or sets the model.
        /// </summary>
        /// <value>
        /// The model.
        /// </value>
        public string Model { get; set; }

        /// <summary>
        /// Gets or sets the warranty date.
        /// </summary>
        /// <value>
        /// The warranty date.
        /// </value>
        public string WarrantyDate { get; set; }

        /// <summary>
        /// Gets or sets the type of the alternative number.
        /// </summary>
        /// <value>
        /// The type of the alternative number.
        /// </value>
        public string AlternativeNumberType { get; set; }

        /// <summary>
        /// Gets or sets the alternative number.
        /// </summary>
        /// <value>
        /// The alternative number.
        /// </value>
        public string AlternativeNumber { get; set; }

        /// <summary>
        /// Gets or sets the designer.
        /// </summary>
        /// <value>
        /// The designer.
        /// </value>
        public string Designer { get; set; }

    }
}
