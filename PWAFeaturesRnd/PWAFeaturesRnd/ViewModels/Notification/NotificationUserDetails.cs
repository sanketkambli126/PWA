namespace PWAFeaturesRnd.ViewModels.Notification
{
	/// <summary>
	/// Notification User Details
	/// </summary>
	public class NotificationUserDetails
	{
		/// <summary>
		/// Gets or sets the username.
		/// </summary>
		/// <value>
		/// The username.
		/// </value>
		public string Username { get; set; }

		/// <summary>
		/// Gets or sets the short name of the user.
		/// </summary>
		/// <value>
		/// The short name of the user.
		/// </value>
		public string UserShortName { get; set; }

		/// <summary>
		/// Gets or sets the name of the primary role.
		/// </summary>
		/// <value>
		/// The name of the primary role.
		/// </value>
		public string PrimaryRoleName { get; set; }
	}
}
