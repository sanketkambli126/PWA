		/// </value>
		public string Description { get; set; }

		/// <summary>
		/// Gets or sets the short code.
		/// </summary>
		/// <value>
		/// The short code.
		/// </value>
		public string ShortCode { get; set; }

		/// <summary>
		/// Gets or sets the type.
		/// </summary>
		/// <value>
		/// The type.
		/// </value>
		public string Type { get; set; }

		/// <summary>
		/// Gets or sets the coy identifier.
		/// </summary>
		/// <value>
