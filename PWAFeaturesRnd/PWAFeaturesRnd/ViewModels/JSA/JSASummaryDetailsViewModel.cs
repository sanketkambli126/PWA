namespace PWAFeaturesRnd.ViewModels.JSA
{
    /// <summary>
    /// JSA Summary Details ViewModel
    /// </summary>
    public class JSASummaryDetailsViewModel
    {
        /// <summary>
        /// Gets or sets the name of the jsa.
        /// </summary>
        /// <value>
        /// The name of the jsa.
        /// </value>
        public string JsaName { get; set; }

        /// <summary>
        /// Gets or sets the responsibility.
        /// </summary>
        /// <value>
        /// The responsibility.
        /// </value>
        public string Responsibility { get; set; }

        /// <summary>
        /// Gets or sets the system area.
        /// </summary>
        /// <value>
        /// The system area.
        /// </value>
        public string SystemArea { get; set; }

        /// <summary>
        /// Gets or sets the created by.
        /// </summary>
        /// <value>
        /// The created by.
        /// </value>
        public string CreatedBy { get; set; }

        /// <summary>
        /// Gets or sets the meeting date.
        /// </summary>
        /// <value>
        /// The meeting date.
        /// </value>
        public string MeetingDate { get; set; }

        /// <summary>
        /// Gets or sets the proposed start date.
        /// </summary>
        /// <value>
        /// The proposed start date.
        /// </value>
        public string ProposedStartDate { get; set; }

        /// <summary>
        /// Gets or sets the estimated end date.
        /// </summary>
        /// <value>
        /// The estimated end date.
        /// </value>
        public string EstimatedEndDate { get; set; }

        /// <summary>
        /// Gets or sets the emergency communication protocol.
        /// </summary>
        /// <value>
        /// The emergency communication protocol.
        /// </value>
        public string EmergencyCommunicationProtocol { get; set; }

        /// <summary>
        /// Gets or sets the communication protocol description.
        /// </summary>
        /// <value>
        /// The communication protocol description.
        /// </value>
        public string CommunicationProtocolDescription { get; set; }

        public string OfficeComments { get; set; }

        public string Reasons { get; set; }

        public string Details { get; set; }
    }
}
